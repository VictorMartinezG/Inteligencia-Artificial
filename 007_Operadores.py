# Programa que realiza operaciones básicas entre dos números
num1 = 12
num2 = 4

suma = num1 + num2  # Suma
resta = num1 - num2  # Resta
multiplicacion = num1 * num2  # Multiplicación
division = num1 / num2  # División clásica

print(f"Suma: {suma}, Resta: {resta}, Multiplicación: {multiplicacion}, División: {division}")
# Salida: Suma: 16, Resta: 8, Multiplicación: 48, División: 3.0
