
#Entero
a = 10
b = 5
suma = a + b

print(f"La suma de {a} y {b} es {suma}.")  
# Salida: La suma de 10 y 5 es 15.

#Flotante
radio = 2.5
pi = 3.1416
area = pi * (radio ** 2)

print(f"El área de un círculo con radio {radio} es {area:.2f}.")  
# Salida: El área de un círculo con radio 2.5 es 19.63.

