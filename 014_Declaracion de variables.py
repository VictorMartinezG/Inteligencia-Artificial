

# Definiendo variables de diferentes tipos
nombre = "Juan"  # Variable tipo cadena (str)
edad = 25  # Variable tipo entero (int)
altura = 1.75  # Variable tipo flotante (float)
es_estudiante = True  # Variable tipo booleano (bool)

# Mostrando los valores y sus tipos
print(f"Nombre: {nombre} (tipo: {type(nombre)})")
print(f"Edad: {edad} años (tipo: {type(edad)})")
print(f"Altura: {altura} m (tipo: {type(altura)})")
print(f"¿Es estudiante?: {es_estudiante} (tipo: {type(es_estudiante)})")
