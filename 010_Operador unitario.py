saldo = 500  # Saldo positivo
deuda = -200  # Representamos la deuda con un signo negativo

# Uso de operadores unarios
nuevo_saldo = saldo + deuda  # Aquí sumamos saldo y deuda (como número negativo)

print(f"Saldo después de pagar la deuda: {nuevo_saldo}")
# Salida: Saldo después de pagar la deuda: 300
