
# Funcion del print
print("hola mundo") # Mortar hola mundo en la terminal 

#Funciones integradas
print(len("Python"))  # Devuelve 6, porque "Python" tiene 6 caracteres.
print(type(3.14))  # Devuelve <class 'float'>, porque 3.14 es un número decimal.

# Uso de sep y end
print("Hola", "Mundo", "!")  # Imprime: Hola Mundo !
print("Python", "es", "cool", sep="-")  # Imprime: Python-es-cool
print("Este mensaje", end=" ")  # No cambia de línea
print("sigue en la misma línea.")  # Se imprimirá en la misma línea.

